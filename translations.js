// Language switcher functionality
let currentLanguage = localStorage.getItem('language') || 'en';

function setLanguage(lang) {
    currentLanguage = lang;
    localStorage.setItem('language', lang);
    document.documentElement.lang = lang;
    
    // Update all elements with data-translate attribute
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        const keys = key.split('.');
        let translation = translations[lang];
        
        for (const k of keys) {
            if (translation && translation[k]) {
                translation = translation[k];
            } else {
                translation = null;
                break;
            }
        }
        
        if (translation) {
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = translation;
            } else if (element.tagName === 'LABEL' || element.tagName === 'BUTTON') {
                element.textContent = translation;
            } else {
                // Check if translation contains HTML tags
                if (typeof translation === 'string' && translation.includes('<')) {
                    element.innerHTML = translation;
                } else {
                    element.textContent = translation;
                }
            }
        }
    });
    
    // Update language button text
    const langBtnText = document.querySelector('#lang-btn span[data-translate="nav.language"]');
    if (langBtnText) {
        langBtnText.textContent = translations[lang].nav.language;
    }
    
    // Update page title
    document.title = `Henrik Velnom | Portfolio - ${translations[lang].nav.language}`;
}

// Initialize language on page load
function initLanguageSwitcher() {
    setLanguage(currentLanguage);
    
    // Language switcher button
    const langBtn = document.getElementById('lang-btn');
    const langDropdown = document.getElementById('lang-dropdown');
    
    if (langBtn && langDropdown) {
        // Remove existing event listeners by cloning
        const newLangBtn = langBtn.cloneNode(true);
        langBtn.parentNode.replaceChild(newLangBtn, langBtn);
        
        const newLangDropdown = langDropdown.cloneNode(true);
        langDropdown.parentNode.replaceChild(newLangDropdown, langDropdown);
        
        // Re-query elements
        const btn = document.getElementById('lang-btn');
        const dropdown = document.getElementById('lang-dropdown');
        
        if (btn && dropdown) {
            btn.addEventListener('click', (e) => {
                e.stopPropagation();
                e.preventDefault();
                dropdown.classList.toggle('active');
            });
            
            // Close dropdown when clicking outside
            document.addEventListener('click', (e) => {
                if (!btn.contains(e.target) && !dropdown.contains(e.target)) {
                    dropdown.classList.remove('active');
                }
            });
            
            // Language option buttons
            document.querySelectorAll('.lang-option').forEach(option => {
                option.addEventListener('click', (e) => {
                    e.stopPropagation();
                    e.preventDefault();
                    const lang = option.getAttribute('data-lang');
                    if (lang) {
                        setLanguage(lang);
                        dropdown.classList.remove('active');
                    }
                });
            });
        }
    }
}

// Try to initialize immediately if DOM is ready
if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', initLanguageSwitcher);
} else {
    // DOM is already ready
    initLanguageSwitcher();
}
